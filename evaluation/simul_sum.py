#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Aggregate task-level evaluation results to simulation-level summaries.

This script reads the existing task-level CSV files generated by tabulate.py
and aggregates them by simulation (dataset), providing weighted averages
of all metrics across all tasks within the simulation.

Usage
-----
python evaluation/simul_wise_sum.py -d euler_1d

Output: Creates two files in eval_results/euler_1d/:
- euler_1d_sum.csv (combined results with precision_level column)
- euler_1d_sum.xlsx (beautifully formatted with colors and visual separators)
"""

import argparse
import csv
import pandas as pd
from pathlib import Path
from typing import Dict, List, Tuple
from statistics import mean


def is_number(v) -> bool:
    """Return True if v can be cast to float."""
    try:
        float(v)
        return True
    except (ValueError, TypeError):
        return False


def weighted_average(values: List[float], weights: List[int]) -> float:
    """Calculate weighted average."""
    if not values or not weights or sum(weights) == 0:
        return 0.0
    return sum(v * w for v, w in zip(values, weights)) / sum(weights)


def aggregate_simulation_results(csv_file: Path, precision_level: str) -> List[Dict]:
    """
    Aggregate task-level results to simulation-level results.
    
    Args:
        csv_file: Path to task-level CSV file
        precision_level: The precision level (high, medium, low)
        
    Returns:
        List of dictionaries with simulation-level aggregated results
    """
    # Read the CSV file
    df = pd.read_csv(csv_file)
    
    # Group by Model and Inference Mode (aggregate across tasks)
    aggregated_results = []
    
    for (model, mode), group in df.groupby(['Model', 'Inference Mode']):
        # Calculate total samples across all tasks
        total_samples = group['Number of Samples'].sum()
        
        # Prepare the aggregated row
        agg_row = {
            'Model': model,
            'Simulation': 'euler_1d',  # Fixed for now, can be made dynamic later
            'Precision Level': precision_level,
            'Inference Mode': mode,
            'Number of Samples': total_samples
        }
        
        # Get sample counts for weighting
        weights = group['Number of Samples'].tolist()
        
        # Aggregate numeric metrics
        for col in group.columns:
            if col in ['Model', 'Task', 'Inference Mode', 'Number of Samples']:
                continue
                
            # Extract numeric values and their corresponding weights
            numeric_values = []
            numeric_weights = []
            
            for idx, value in enumerate(group[col]):
                if is_number(value):
                    numeric_values.append(float(value))
                    numeric_weights.append(weights[idx])
            
            if numeric_values:
                # For cost fields, use sum instead of weighted average
                if col in ['total_dummy_cost', 'total_model_cost']:
                    # These are cumulative metrics - sum across tasks
                    agg_row[col] = sum(numeric_values)
                else:
                    # For rate/efficiency metrics, use weighted average
                    agg_row[col] = weighted_average(numeric_values, numeric_weights)
            else:
                # If no numeric values, use the first non-empty value or 'nan'
                non_empty_values = [v for v in group[col] if str(v).strip() and str(v) != 'nan']
                agg_row[col] = non_empty_values[0] if non_empty_values else 'nan'
        
        aggregated_results.append(agg_row)
    
    return aggregated_results


def write_simulation_csv(results: List[Dict], output_file: Path) -> None:
    """Write simulation-level results to CSV."""
    if not results:
        return
        
    # Determine column order (similar to tabulate.py)
    priority_cols = [
        'Model', 'Simulation', 'Precision Level', 'Inference Mode', 'Number of Samples',
        'converged_rate', 'success_rate', 'mean_soft_success', 'mean_efficiency', 
        'mean_hard_efficiency', 'mean_rmse', 'rmse_tolerance'
    ]
    
    # Get all columns from results
    all_cols = set()
    for result in results:
        all_cols.update(result.keys())
    
    # Order columns: priority first, then remaining alphabetically
    remaining_cols = sorted(all_cols - set(priority_cols))
    ordered_cols = [col for col in priority_cols if col in all_cols] + remaining_cols
    
    # Write CSV
    output_file.parent.mkdir(parents=True, exist_ok=True)
    with output_file.open('w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=ordered_cols)
        writer.writeheader()
        writer.writerows(results)


def write_simulation_excel(results: List[Dict], output_file: Path) -> None:
    """Write simulation-level results to Excel with beautiful formatting and visual separators."""
    if not results:
        return
        
    # Determine column order
    priority_cols = [
        'Model', 'Simulation', 'Precision Level', 'Inference Mode', 'Number of Samples',
        'converged_rate', 'success_rate', 'mean_soft_success', 'mean_efficiency', 
        'mean_hard_efficiency', 'mean_rmse', 'rmse_tolerance'
    ]
    
    # Get all columns from results
    all_cols = set()
    for result in results:
        all_cols.update(result.keys())
    
    # Order columns: priority first, then remaining alphabetically
    remaining_cols = sorted(all_cols - set(priority_cols))
    ordered_cols = [col for col in priority_cols if col in all_cols] + remaining_cols
    
    # Create DataFrame
    df = pd.DataFrame(results)[ordered_cols]
    
    # Convert numeric columns
    for col in ['mean_efficiency', 'mean_hard_efficiency']:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors='coerce')
    
    # Handle NaN and infinity values
    for col in df.columns:
        df[col] = df[col].apply(lambda x: 'nan' if pd.isna(x) or (isinstance(x, float) and (x == float('inf') or x == -float('inf'))) else x)
    
    # Write Excel with advanced formatting
    output_file.parent.mkdir(parents=True, exist_ok=True)
    with pd.ExcelWriter(output_file, engine='xlsxwriter') as writer:
        wb = writer.book
        ws = wb.add_worksheet('Simulation Summary')
        writer.sheets['Simulation Summary'] = ws
        
        # Simple formatting styles - no colors, just structure
        header_fmt = wb.add_format({
            'bold': True,
            'border': 1,
            'align': 'center',
            'valign': 'vcenter'
        })
        
        # Standard cell format
        standard_fmt = wb.add_format({
            'border': 1
        })
        
        # Best performance highlighting - only bold
        best_performance_fmt = wb.add_format({
            'bold': True,
            'border': 1
        })
        
        # Write header row with formatting
        for i, col in enumerate(ordered_cols):
            ws.write(0, i, col, header_fmt)
        
        excel_row = 1
        col_idx = {c: i for i, c in enumerate(ordered_cols)}
        
        # Sort by Precision Level and Inference Mode for organized display
        df_sorted = df.sort_values(['Precision Level', 'Inference Mode'])
        
        current_precision = None
        current_mode = None
        
        for _, row in df_sorted.iterrows():
            precision = row['Precision Level']
            mode = row['Inference Mode']
            
            # Add visual separator (blank row) when precision level changes
            if current_precision != precision and current_precision is not None:
                excel_row += 1
            current_precision = precision
            
            # Add visual separator (blank row) when inference mode changes within same precision
            if current_mode != mode and current_mode is not None:
                excel_row += 1
            current_mode = mode
            
            # Find best efficiency within current precision/mode group for highlighting
            same_group = df_sorted[
                (df_sorted['Precision Level'] == precision) & 
                (df_sorted['Inference Mode'] == mode)
            ]
            max_efficiency = None
            if 'mean_efficiency' in same_group.columns:
                numeric_efficiencies = same_group['mean_efficiency'][same_group['mean_efficiency'] != 'nan']
                if len(numeric_efficiencies) > 0:
                    max_efficiency = numeric_efficiencies.max()
            
            # Check if this is the best performing model in this group
            is_best_performance = (
                max_efficiency is not None
                and row.get('mean_efficiency', 0) == max_efficiency
                and row.get('mean_efficiency', 0) != 0
                and row.get('mean_efficiency', 0) != 'nan'
            )
            
            # Write each cell with appropriate formatting
            for i, col in enumerate(ordered_cols):
                val = row[col]
                
                # Choose formatting: best performance gets bold, others use standard format
                if is_best_performance and col in ['Model', 'mean_efficiency']:
                    cell_fmt = best_performance_fmt
                else:
                    cell_fmt = standard_fmt
                
                # Write cell value
                if col == 'Number of Samples':
                    ws.write_number(excel_row, i, val, cell_fmt)
                elif val == 'nan':
                    ws.write(excel_row, i, 'nan', cell_fmt)
                else:
                    ws.write(excel_row, i, val, cell_fmt)
            
            excel_row += 1
        
        # Auto filter and freeze panes
        ws.autofilter(0, 0, excel_row - 1, len(ordered_cols) - 1)
        ws.freeze_panes(1, 0)
        
        # Auto-adjust column widths with professional spacing
        for idx, col in enumerate(ordered_cols):
            if col == 'Model':
                ws.set_column(idx, idx, 35)  # Wider for model names
            elif col in ['Precision Level', 'Inference Mode']:
                ws.set_column(idx, idx, 15)  # Medium width for categories
            elif col == 'Simulation':
                ws.set_column(idx, idx, 12)  # Compact for simulation
            else:
                # Auto-size other columns with reasonable bounds
                max_len = max(df[col].astype(str).map(len).max(), len(col)) + 3
                ws.set_column(idx, idx, min(max_len, 20))
        
        # Add a simple note at the bottom
        legend_row = excel_row + 2
        legend_fmt = wb.add_format({'italic': True, 'font_size': 9})
        ws.write(legend_row, 0, "Note: Best efficiency model in each precision/mode group shown in bold", legend_fmt)
        ws.write(legend_row + 1, 0, "Blank rows separate precision levels and inference modes", legend_fmt)


def main():
    parser = argparse.ArgumentParser(
        description="Aggregate task-level results to simulation-level summaries"
    )
    parser.add_argument(
        "-d", "--dataset", required=True, 
        help="Dataset name (e.g. euler_1d)"
    )
    args = parser.parse_args()
    
    # Validate dataset directory
    eval_root = Path("eval_results")
    dataset_dir = eval_root / args.dataset
    if not dataset_dir.is_dir():
        available = ", ".join(sorted(p.name for p in eval_root.iterdir() if p.is_dir()))
        print(f"❌ Dataset '{args.dataset}' not found in {eval_root}")
        print(f"✅ Available datasets: {available}")
        return
    
    # Currently only support euler_1d
    if args.dataset != "euler_1d":
        print(f"❌ Currently only supports euler_1d dataset")
        return
    
    # Process each precision level and combine results
    precision_levels = ["high", "medium", "low"]
    all_results = []
    
    for precision in precision_levels:
        # Input CSV file from tabulate.py
        input_csv = dataset_dir / f"{args.dataset}_{precision}_summary.csv"
        
        if not input_csv.exists():
            print(f"⚠️  Input file not found: {input_csv}")
            continue
        
        # Aggregate results for this precision level
        print(f"Processing {precision} precision level...")
        precision_results = aggregate_simulation_results(input_csv, precision)
        
        if not precision_results:
            print(f"⚠️  No results to aggregate for {precision} precision level")
            continue
        
        all_results.extend(precision_results)
        print(f"✅ Processed {len(precision_results)} model results for {precision} precision")
    
    if not all_results:
        print("❌ No results to write")
        return
    
    # Output files in the main dataset directory
    output_csv = dataset_dir / f"{args.dataset}_sum.csv"
    output_xlsx = dataset_dir / f"{args.dataset}_sum.xlsx"
    
    # Write combined outputs
    print(f"\nWriting combined results...")
    write_simulation_csv(all_results, output_csv)
    write_simulation_excel(all_results, output_xlsx)
    
    print(f"\n🎉 Simulation-level summaries generated:")
    print(f"   📊 CSV  : {output_csv}")
    print(f"   📈 Excel: {output_xlsx} (beautifully formatted with colors and visual separators)")
    print(f"\n📋 Summary: Combined {len(all_results)} model results across {len(precision_levels)} precision levels")


if __name__ == "__main__":
    main()